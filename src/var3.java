/*
数据类型布尔值boolean举例
 */
public class var3 {
    public static void main(String[] args) {
        /*
        double money = 1000;
        double price = 2000;
        System.out.println(money > price);//false
         */

        /*
        double money = 3000;
        double price = 2000;
        System.out.println(money > price);//true
         */

        /*
        double money = 1000;
        double price = 2000;
        System.out.println(money < price);//true
         */

        /*
        double money = 3000;
        double price = 2000;
        System.out.println(money < price);//false
         */

        // =赋值，不是数学上的等于
        // == 条件判断，判断两边的值是否相同
        /*
        double money = 2000;
        double price = 2000;
        System.out.println(money == price);//true
         */

        /*
        double money = 1000;
        double price = 2000;
        System.out.println(money == price);//false
         */

        //!= 判断左右两端是否不一致
        /*
        double money = 2000;
        double price = 2000;
        System.out.println(money != price);//false
         */

        /*
        double money = 1000;
        double price = 2000;
        System.out.println(money != price);//true
         */
    }
}

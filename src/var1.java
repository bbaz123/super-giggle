/*
数据类型整数int举例
 */
public class var1 {
    public static void main(String[] args) {
      /*
       // 声明一个int变量
        int a =10;
        System.out.println(a);
        a =20; //改变变量a的值
        System.out.println(a);
       */
        int a =10;
        int b =20;
        System.out.println(a+b);//加
        System.out.println(a-b);//减
        System.out.println(a*b);//乘
        System.out.println(a/b);//如果运算两端都是int，结果就是int，整除的结果,所以运算结果为0（10/20=0....10）
        System.out.println(a%b);//计算余数
        System.out.println(10%3);//计算余数案例
    }
}
